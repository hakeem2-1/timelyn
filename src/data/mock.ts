function task(
  num: number,
  partial: Omit<
    import("@/types").SheetTask,
    "id" | "taskNumber" | "assignedAt" | "updatedAt"
  > & { id?: string; assignedAt?: string; updatedAt?: string }
): import("@/types").SheetTask {
  const now = new Date().toISOString();
  return {
    id: partial.id ?? generateId(),
    taskNumber: num,
    assignedAt: partial.assignedAt ?? now,
    updatedAt: partial.updatedAt ?? now,
    title: partial.title,
    description: partial.description,
    adminNotes: partial.adminNotes,
    referenceImageUrl: partial.referenceImageUrl,
    rarity: partial.rarity,
    urgency: partial.urgency,
    status: partial.status,
    employeeNotes: partial.employeeNotes,
    completionNote: partial.completionNote,
    completedAt: partial.completedAt,
  };
}

